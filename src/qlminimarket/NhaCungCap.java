    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        if (validateForm()) {
            nhacungcap sp = getModel();
            try {
                if (controll.add(sp) > 0) {
                    JOptionPane.showMessageDialog(this, "Thêm thành công");
                    fillDataTable();
                } else {
                    JOptionPane.showMessageDialog(this, "Trùng ID");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Lỗi:" + e.toString());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Bạn hãy nhập đầy đủ thông tin");
        }
    }//GEN-LAST:event_addBtnActionPerformed

    private void editBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editBtnActionPerformed
        if (validateForm()) {
            nhacungcap ncc = getModel();
            try {
                if (controll.edit(ncc) > 0) {
                    JOptionPane.showMessageDialog(this, "Sửa thành công");
                    fillDataTable();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Lỗi:" + e.toString());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Bạn hãy nhập đầy đủ thông tin");
        }
    }//GEN-LAST:event_editBtnActionPerformed

    private void delBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delBtnActionPerformed
        if (txtID.getText().isEmpty() || id_NCC.length() == 0) {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập id để xóa");
        } else {
            try {
                if (id_NCC.length() > 0) {
                    controll.del(id_NCC);
                    JOptionPane.showMessageDialog(this, "Xóa thành công");
                } else {
                    controll.del(txtID.getText());
                    JOptionPane.showMessageDialog(this, "Xóa thất bại");
                }
                fillDataTable();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Lỗi:" + e.toString());
            }
        }
    }//GEN-LAST:event_delBtnActionPerformed

