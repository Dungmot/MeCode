    public void fillDataTable() {
        DefaultTableModel model = (DefaultTableModel) tbNCC.getModel();
        model.setRowCount(0); //clear table
        for (nhacungcap sp : controll.getAllNCC()) {
            Object dataRow[] = new Object[7];
            dataRow[0] = sp.getId();
            dataRow[1] = sp.getTen();
            dataRow[2] = sp.getDiachi();
            dataRow[3] = sp.getNhom();
            dataRow[4] = sp.getSdt();
            dataRow[5] = sp.getTonggiaodich();
            dataRow[6] = sp.getDuno();
            model.addRow(dataRow);
        }
        txtID.requestFocus();
    }

    private void resetFrom() {
        txtID.setText("");
        txtTen.setText("");
        txtDiaChi.setText("");
        txtNhom.setText("");
        txtSDT.setText("");
        txtTongGiaoDich.setText("");
        txtDuno.setText("");
    }

    public void setModel(nhacungcap ncc) {
        txtID.setText(ncc.getId());
        txtTen.setText(ncc.getTen());
        txtDiaChi.setText(ncc.getDiachi());
        txtNhom.setText(ncc.getNhom());
        spLoai.setSelectedItem(ncc.getNhom());
        txtSDT.setText(ncc.getSdt());
        txtTongGiaoDich.setText(String.valueOf(ncc.getTonggiaodich()));
        txtDuno.setText(String.valueOf(ncc.getDuno()));
    }

    public nhacungcap getModel() {
        nhacungcap ncc = new nhacungcap();
        ncc.setId(txtID.getText());
        ncc.setTen(txtTen.getText());
        ncc.setDiachi(txtDiaChi.getText());
        ncc.setNhom((String) spLoai.getSelectedItem());
        ncc.setSdt(txtSDT.getText());
        ncc.setTonggiaodich(Integer.parseInt(txtTongGiaoDich.getText()));
        ncc.setDuno(Integer.parseInt(txtDuno.getText()));
        return ncc;
    }

    public boolean validateForm() {
        if (txtID.getText().isEmpty() || txtTen.getText().isEmpty() || txtNhom.getText().toString().isEmpty() || txtDiaChi.getText().isEmpty() || txtSDT.getText().isEmpty()) {
            return false;
        }
        return true;
    }

    private void showDataCombobox() {
        String sSQL = "SELECT LOAI_SP from CHI_TIET_SAN_PHAM order by LOAI_SP desc";
        try {
            Connection conn = ConnectDB.getDBConnect();
            PreparedStatement sttm = conn.prepareStatement(sSQL);
            ResultSet rs = sttm.executeQuery();
            while (rs.next()) {
                this.spLoai.addItem(rs.getString("LOAI_SP"));
            }
        } catch (Exception e) {
        }
    }

