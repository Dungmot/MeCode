    private void UserTBMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_UserTBMouseClicked
        int id = UserTB.rowAtPoint(evt.getPoint());
        id_NV = UserTB.getValueAt(id, 0).toString();
        try {
            login nv = contr.findByID(id_NV);
            setModel(nv);
        } catch (Exception ex) {
            Logger.getLogger(NhanVien.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_UserTBMouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (txtID.getText().isEmpty() || id_NV.length()==0) {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập id để xóa");
        }else{
            try {
                if (id_NV.length()>0) {
                    contr.del(id_NV);
                }else{
                    contr.del(txtID.getText());
                }
                fillDataTable();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Lỗi:"+e.toString());
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

