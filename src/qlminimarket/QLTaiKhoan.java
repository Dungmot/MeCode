    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (validateForm()){
            login nv = getModel();
            try {
                if (contr.add(nv) > 0) {
                    JOptionPane.showMessageDialog(this, "Thêm thành công");
                    fillDataTable();
                } else{
                    JOptionPane.showMessageDialog(this, "Trùng tên ID");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Lỗi:"+e.toString());
            }
        }else{
            JOptionPane.showMessageDialog(this, "Bạn hãy nhập đầy đủ thông tin");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void editBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editBtnActionPerformed
        if (validateForm()){
            login lg = getModel();
            try {
                if (contr.edit(lg) > 0) {
                    JOptionPane.showMessageDialog(this, "Sửa thành công");
                    fillDataTable();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Lỗi:"+e.toString());
            }
        }else{
            JOptionPane.showMessageDialog(this, "Bạn hãy nhập đầy đủ thông tin");
        }
    }//GEN-LAST:event_editBtnActionPerformed

    public void setModel(login user){
        txtID.setText(user.getId());
        txtPw.setText(user.getPw());
        RoleBtn.setSelectedItem(user.getVaitro());
    }
    public login getModel(){
       login nv = new login();
       nv.setId(txtID.getText());
       nv.setPw(txtPw.getText());
       nv.setVaitro((String) RoleBtn.getSelectedItem());
       return  nv;
    }
    
    public boolean validateForm(){
        if (txtID.getText().isEmpty()||txtPw.getText().isEmpty()){
            return false;
        }
        return true;
    } 
    
    
