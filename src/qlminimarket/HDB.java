    private void btnSAVEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSAVEActionPerformed
        if (validateFormHD()) {
            hoadon hd = getModel();
            khachhang kh = getModelDiem();
            try {
                if (controll.addHD(hd) > 0) {
                    JOptionPane.showMessageDialog(this, "Thêm thành công");
                    showtbHD();
                    if (controllKH.editDiem(kh) > 0) {
                        JOptionPane.showMessageDialog(this, "Cộng Điểm thành công");
                    }
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Lỗi:" + e.toString());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Bạn hãy nhập đầy đủ thông tin");
        }
    }//GEN-LAST:event_btnSAVEActionPerformed

    public void bill() {
        int khachtra = Integer.parseInt(txtKhachtra.getText());
        int tralai = Integer.parseInt(txtTraLai.getText());
        int diem = Integer.parseInt(txtDiem.getText());
        int phaitra = Integer.parseInt(txtPhaittra.getText());
        String date = txtDate.getText();
        String sohd = txtMaHD.getText();
        String tennv = txtKH.getText();
        DefaultTableModel model = (DefaultTableModel) tbSPchitiet.getModel();

        //hEADING    
        txtBill.setText(txtBill.getText() + "                                GDL MARKET\n");
        txtBill.setText(txtBill.getText() + "                141 Chiến Thắng, Thanh Trì, Hà Nội\n");
        txtBill.setText(txtBill.getText() + "                             SDT: 0999999999\n");
        txtBill.setText(txtBill.getText() + " ------------------------------------------------------------------------------\n");
        txtBill.setText(txtBill.getText() + "                        HÓA ĐƠN THANH TOÁN                      \n");
        txtBill.setText(txtBill.getText() + "\t      Số hóa đơn: " + sohd + "\n");
        txtBill.setText(txtBill.getText() + " Ngày lập:\t" + date + "\n");
        txtBill.setText(txtBill.getText() + " Khách hàng:\t" + tennv + "\n");
        txtBill.setText(txtBill.getText() + " ------------------------------------------------------------------------------\n");
        txtBill.setText((txtBill.getText() + " Sản phẩm" + "\t" + "Số lượng" + "\t" + "Đơn giá" + "\t" + "Thành tiền" + "\n"));
        txtBill.setText(txtBill.getText() + " ------------------------------------------------------------------------------\n");

        for (int i = 0; i < model.getRowCount(); i++) {
            String tensp = (String) model.getValueAt(i, 2);
            int sl = (int) model.getValueAt(i, 3);
            int gia = (int) model.getValueAt(i, 4);
            int thanhtien = (int) model.getValueAt(i, 5);
            txtBill.setText((txtBill.getText() + " " + tensp + "\t" + sl + "\t" + gia + "\t" + thanhtien + "\n"));
        }
        txtBill.setText(txtBill.getText() + "\n\n");
        txtBill.setText(txtBill.getText() + " ------------------------------------------------------------------------------\n");
        txtBill.setText(txtBill.getText() + "\t\t" + "    Tổng tiền: " + phaitra + "\n");
        txtBill.setText(txtBill.getText() + "\t\t" + "    Khách trả: " + khachtra + "\n");
        txtBill.setText(txtBill.getText() + "\t\t" + "    Tiền thừa: " + tralai + "\n");
        txtBill.setText(txtBill.getText() + "\t\t" + "    Điểm tích: " + diem + "\n\n");
        txtBill.setText(txtBill.getText() + "\t     Xin cảm ơn quý khách\n");
        txtBill.setText(txtBill.getText() + "\t              hẹn gặp lại\n");

    }

