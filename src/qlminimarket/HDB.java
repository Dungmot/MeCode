    private void btnADDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnADDActionPerformed
        sanpham sp1 = getModelSLsp();
        String masp = txtMaSP.getText();
        String tensp = txtTenSP.getText();
        int sl = Integer.parseInt(txtSLmua.getText());
        int gia = Integer.parseInt(txtgiaSP.getText());
        int thanhtien = Integer.parseInt(txthanhtien1.getText());
        try {
            //update SL sp vào db
            if (spController.editSLSP(sp1) > 0) {
                //Thêm sp mua hàng vào bảng
                sanphamCT sp = new sanphamCT(masp, tensp, sl, gia, thanhtien);
                new hdbControll().addsanphamCT(sp);
                JOptionPane.showMessageDialog(this, "Đã update số lượng hàng trong kho");
                showtbSPchitiet();
                txtSLcon.setText("");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi:" + e.toString());
        }
    }//GEN-LAST:event_btnADDActionPerformed

