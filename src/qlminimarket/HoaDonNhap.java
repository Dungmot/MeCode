    private void btnADDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnADDActionPerformed
        String masp = txtMaSP.getText();
        String tensp = txtTenSP.getText();
        int sl = Integer.parseInt(txtSLnhap.getText());
        int gia = Integer.parseInt(txtgiaSP.getText());
        int thanhtien = Integer.parseInt(txthanhtien1.getText());
        sanpham sp1 = getModelSLsp();
        //update SL sp vào db
        if (spController.editSLSP(sp1) > 0) {
            //Thêm sp mua hàng vào bảng
            hoadonnhap_sp sp = new hoadonnhap_sp(masp, tensp, sl, gia, thanhtien);
            new hoadonnhapControll().addsanphamCT(sp);
            showtbSPchitiet();
            showtbSPtonkho();
            JOptionPane.showMessageDialog(this, "Đã update số lượng hàng trong kho");
            txtSLcon.setText("");
        }
    }//GEN-LAST:event_btnADDActionPerformed

