    private void btnSAVEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSAVEActionPerformed
        if (validateFormHD()) {
            hoadonnhap hd = getModel();
            nhacungcap ncc = getModelNCC();
            try {
                if (controll.addHD(hd) > 0) {
                    JOptionPane.showMessageDialog(this, "Thêm thành công");
                    showtbHD();
                    if (nccControll.editNCC(ncc) > 0) {
                        JOptionPane.showMessageDialog(this, "Đã update thông tin nhà cung cấp");
                    }

                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Lỗi:" + e.toString());
                JOptionPane.showMessageDialog(this, "Trùng ID hóa đơn");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Bạn hãy nhập đầy đủ thông tin");
        }
    }//GEN-LAST:event_btnSAVEActionPerformed

