    private void showtbHD() {
        hoadon = new hoadonnhapControll().getListHD();
        ModelHD.setRowCount(0);
        for (hoadonnhap hd : hoadon) {
            ModelHD.addRow(new Object[]{
                hd.getMaHD(), hd.getDateOrder(), hd.getTongtien()
            });
        }
    }

    private void showtbSPchitiet() {
        sanphamCT = new hoadonnhapControll().getAllsp();
        ModelspCT.setRowCount(0);
        for (hoadonnhap_sp sp : sanphamCT) {
            ModelspCT.addRow(new Object[]{
                tbSPchitiet.getRowCount() + 1, sp.getMasp(), sp.getTensp(), sp.getSoluong(), sp.getGia(), sp.getThanhtien()
            });
        }
        int sum = 0;
        for (int i = 0; i < tbSPchitiet.getRowCount(); i++) {
            sum = sum + Integer.parseInt(tbSPchitiet.getValueAt(i, 5).toString());
        }
        txtTongtien.setText(Integer.toString(sum));
        txtMaSP.setText("");
        txtgiaSP.setText("");
        txtTenSP.setText("");
        txtSLnhap.setText("");
        txthanhtien1.setText("");
        txtMaSP.requestFocus();
    }

