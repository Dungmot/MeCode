    private void tbSPTrongKhoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbSPTrongKhoMouseClicked
        int id = tbSPTrongKho.rowAtPoint(evt.getPoint());
        String id_SP = tbSPTrongKho.getValueAt(id, 0).toString();
        try {
            sanpham sp = spController.findByID(id_SP);
            setModelSP(sp);
            nhacungcap ncc = nccControll.findByNhom((String) txtLoai.getSelectedItem());
            setModelNCC(ncc);
        } catch (Exception ex) {
            Logger.getLogger(SanPham.class.getName()).log(Level.SEVERE, null, ex);
            int result = JOptionPane.showConfirmDialog(this,
                    "Không tìm thấy\nBạn có muốn thêm nhà cung cấp?",
                    "Xác nhận",
                    JOptionPane.YES_NO_OPTION,
                    JOptionPane.QUESTION_MESSAGE);
            if (result == JOptionPane.YES_OPTION) {
                NhaCungCap sp = new NhaCungCap();
                sp.setVisible(true);
            } else if (result == JOptionPane.NO_OPTION) {
            }
        }
    }//GEN-LAST:event_tbSPTrongKhoMouseClicked

