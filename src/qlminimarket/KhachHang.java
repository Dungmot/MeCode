    public boolean validateForm() {
        if (khTen.getText().isEmpty() || khSDT.getText().isEmpty()) {
            return false;
        }
        return true;
    }

    public khachhang getModel() {
        khachhang kh = new khachhang();
        kh.setId(Integer.parseInt(khID.getText()));
        kh.setTen(khTen.getText());
        kh.setSdt(khSDT.getText());
        kh.setGt((String) khGT.getText());
        kh.setNgaysinh(khDate.getText());
        kh.setDiem(Integer.parseInt(khDiem.getText()));
        return kh;
    }
    
    public void setModel(khachhang kh){
        khID.setText(String.valueOf(kh.getId()));
        khTen.setText(kh.getTen());
        khSDT.setText(kh.getSdt());
        khGT.setText(kh.getGt());
        khDate.setText(kh.getNgaysinh());
        khDiem.setText(String.valueOf(kh.getDiem()));
    }

