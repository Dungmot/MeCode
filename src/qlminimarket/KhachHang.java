    public void fillDataTable() {
        DefaultTableModel model = (DefaultTableModel) tbKH.getModel();
        model.setRowCount(0);
        for (khachhang kh : controll.getAllkh()) {
            Object dataRow[] = new Object[6];
            dataRow[0] = kh.getId();
            dataRow[1] = kh.getTen();
            dataRow[2] = kh.getSdt();
            dataRow[3] = kh.getGt();
            dataRow[4] = kh.getNgaysinh();
            dataRow[5] = kh.getDiem();
            model.addRow(dataRow);
        }
    }

